class ArticleModel {
  String author;
  String title;
  String description;
  String url;
  String urlToImage;
  String content;

  ArticleModel({String author, String title, String description, String url, String urlToImage, String content}) {
      this.author = author;
      this.title = title;
      this.description = description;
      this.url = url;
      this. urlToImage = urlToImage;
      this.content = content;
  }
}